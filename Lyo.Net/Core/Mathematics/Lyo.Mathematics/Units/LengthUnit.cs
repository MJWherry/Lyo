    Meter = 0,
    /// <summary>Centimeter (10⁻² m).</summary>
    Centimeter = 1,
    /// <summary>Kilometer (10³ m).</summary>
    Kilometer = 2,
    /// <summary>Inch (0.0254 m).</summary>
    Inch = 3,
    /// <summary>International foot (0.3048 m).</summary>
    Foot = 4,
    /// <summary>International mile (1609.344 m).</summary>
    Mile = 5
}